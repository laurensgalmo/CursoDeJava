



public class MetodosClaseString {

   
    public static void main(String[] args) {
        
        String str = "La estupidez insiste siempre";
        
        System.out.println("EL STRING ES: " + str);
        System.out.println("EL TAMAÑO DEL STRING ES: " + str.length());  
        System.out.println("EL CARÁCTER EN LA POSICIÓN 5 ES: " + str.charAt(5));
        System.out.println("EL SUBSTRING DE 3 A 12 ES: " + str.substring(3, 12));
        System.out.println("EL ÍNDEX DEL STRING ES: " + str.indexOf('z'));
        System.out.println("EL STRING EN MAYÚSCULAS: " + str.toUpperCase());
        System.out.println("EL STRING EN MINÚSCULAS: " + str.toLowerCase());
            
    }
    
}
